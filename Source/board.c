/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: void setNode(struct Cnode* node, int x, int y); */
/* parameters: a pointer to a Cnode struct and the position */
/* return value: NULL */
/* description: initialize the node cell */
/* //////////////////////////////////////////////////////////////////////// */
void setNode(struct Cnode* node, int x, int y)
{
	node->m_x = x;
	node->m_y = y;
	node->m_pParent = NULL;
	node->m_next = NULL;
	node->m_G = 0;
	node->m_F = 0;
	node->m_openflag = false;
	node->m_closeflag = false;
	node->m_timepro = 0;
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: void initializeBoardData(struct Cboard* board); */
/* parameters: a pointer to a Cboard struct */
/* return value: NULL */
/* description: initialize the main board */
/* //////////////////////////////////////////////////////////////////////// */
void initializeBoardData(struct Cboard* board)
{
	int i, j;
	struct Cnode* current;
	board->m_xdis = MAPX;
	board->m_ydis = MAPY;

	for (i = 0; i < board->m_xdis; i++)
		for (j = 0; j < board->m_ydis; j++)
		{
		current = (struct Cnode*)malloc(sizeof(struct Cnode));
		setNode(current, i, j);
		board->data[i][j] = current;
		}
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: void resetBoardData(struct Cboard* board); */
/* parameters: a pointer to a Cboard struct */
/* return value: NULL */
/* description: reset the main board */
/* //////////////////////////////////////////////////////////////////////// */
void resetBoardData(struct Cboard* board)
{
	int i, j;
	for (i = 0; i < board->m_xdis; i++)
		for (j = 0; j < board->m_ydis; j++)
		{
		setNode(board->data[i][j], i, j);
		}
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: void boardPointClear(int x, int y); */
/* parameters: a position */
/* return value: NULL */
/* description: when the pacman has eaten a pac just clear the pac int this position */
/* //////////////////////////////////////////////////////////////////////// */
void boardPointClear(int x, int y)
{
	if (orgdata[x][y] == 2 || orgdata[x][y] == 3)
		orgdata[x][y] = 0;
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: void recoverOrgData(); */
/* parameters: NULL */
/* return value: NULL */
/* description: when we reset the game, recover the pacs positions */
/* //////////////////////////////////////////////////////////////////////// */
void recoverOrgData()
{
	int i, j;
	for (i = 0; i < MAPX; i++)
		for (j = 0; j < MAPY; j++)
		{
		orgdata[i][j] = recoverdata[i][j];
		}
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: void clearBoard(struct Cboard* board); */
/* parameters: a pointer to a board struct */
/* return value: NULL */
/* description: before exiting the game, free the space */
/* //////////////////////////////////////////////////////////////////////// */
void clearBoard(struct Cboard* board)
{
	int i, j;
	for (i = 0; i < board->m_xdis; i++)
		for (j = 0; j < board->m_ydis; j++)
		{
		free(board->data[i][j]);
		board->data[i][j] = NULL;
		}
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: bool VerifyPos(struct Cboard* board, struct Cpoint pos) */
/* parameters: a pointer to a board struct and a position */
/* return value: if the position is valid then return true, else return false */
/* description: verify a position */
/* //////////////////////////////////////////////////////////////////////// */
bool VerifyPos(struct Cboard* board, struct Cpoint pos)
{
	if (pos.x<0 || pos.x>board->m_xdis - 1 || pos.y<0 || pos.y>board->m_ydis - 1)
		return false;
	if (orgdata[pos.x][pos.y] == 1)
		return false;

	return true;
}

/* //////////////////////////////////////////////////////////////////////// */
/* function prototype: bool Verify(struct Cboard* board, int x, int y) */
/* parameters: a pointer to a board struct and a position */
/* return value: if the position is valid then return true, else return false */
/* description: verify a position */
/* //////////////////////////////////////////////////////////////////////// */
bool Verify(struct Cboard* board, int x, int y)
{
	if (x<0 || x>board->m_xdis - 1 || y<0 || y>board->m_ydis - 1)
		return false;
	if (orgdata[x][y] == 1)
		return false;

	return true;
}

